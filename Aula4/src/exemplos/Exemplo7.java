package exemplos;

public class Exemplo7 {

	 
	public static void main(String[] args) {
		int mes = 1;
		
		switch (mes) {
		case 1:
			System.out.println("Janeiro");
			break;
		case 2:
			System.out.println("Fevereiro");
			break;
		case 3:
			System.out.println("Marco");
			break;
		case 4:
			System.out.println("Abril");
			break;
		default:
			System.out.println("HA... o resto dos meses...");
			break;
		}
	}

}
